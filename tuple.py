# Fixing Lists 

# TUPLES
# A tuple is a collection which is ordered and unchangeable
# Tuples are written with rounded brackets as opposed to a list which is written with square brackets
days = ('Mon' , 'Tue' , 'Wed' , 'Thu' , 'Fri' , 'Sat' , 'Sun')
print('days :', type(days))

print('days :', days)
print('number of days :', len(days))
print('first day of of week :', days[0])

# another tuple containing some personal details of a user

